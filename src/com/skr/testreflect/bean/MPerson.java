package com.skr.testreflect.bean;

public class MPerson {
    private String name;

    private int age;

    private long longName;

    private Integer otherAge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public long getLongName() {
        return longName;
    }

    public void setLongName(long longName) {
        this.longName = longName;
    }

    public Integer getOtherAge() {
        return otherAge;
    }

    public void setOtherAge(Integer otherAge) {
        this.otherAge = otherAge;
    }
}
